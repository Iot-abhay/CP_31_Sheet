#include <bits/stdc++.h>
using namespace std;


/*================== SOLUTION START =====================*/

void solve()
{
    int n;
    cin >> n;
    if (n % 3)
    {
        cout << "First\n";
    }
    else
    {
        cout << "Second\n";
    }
}

/*================== SOLUTION END =====================*/


int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        solve();
    }
    return 0;
}